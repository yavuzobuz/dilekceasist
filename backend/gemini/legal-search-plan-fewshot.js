const normalizeRiskTag = (value = '') =>
    normalizeMatchText(value).replace(/[^a-z0-9]+/g, '_').replace(/^_+|_+$/g, '').trim();

const normalizeRiskTags = (values = []) => {
    const unique = [];
    const seen = new Set();

    for (const value of Array.isArray(values) ? values : [values]) {
        const normalized = normalizeRiskTag(value);
        if (!normalized || seen.has(normalized)) continue;
        seen.add(normalized);
        unique.push(normalized);
    }

    return unique;
};

