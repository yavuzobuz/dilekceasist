const scoreExample = ({ example, queryMode = 'short_issue', primaryDomain = 'genel_hukuk', riskTags = [], stage = 'planner' } = {}) => {
    let score = 0;
    const normalizedExampleRiskTags = normalizeRiskTags(example?.riskTags || []);
    const riskSet = new Set(normalizeRiskTags(riskTags));

    if (example?.primaryDomain === primaryDomain) score += 100;
    if (example?.queryMode === queryMode) score += 40;
    if (example?.kind === 'positive') score += 20;
    if (stage === 'reviewer' && example?.kind !== 'positive') score += 10;

    for (const riskTag of normalizedExampleRiskTags) {
        if (riskSet.has(riskTag)) score += 15;
    }

    return score;
};

const orderExamples = (examples = [], context = {}) =>
    [...examples].sort((left, right) => scoreExample({ example: right, ...context }) - scoreExample({ example: left, ...context }));

const pushExamples = (target = [], source = [], { limit = 6 } = {}) => {
    const seen = new Set(target.map((item) => item.id));

    for (const example of source) {
        if (!example?.id || seen.has(example.id)) continue;
        seen.add(example.id);
        target.push(example);
        if (target.length >= limit) break;
    }

    return target;
};

export const selectFewShotExamples = ({
    primaryDomain = 'genel_hukuk',
    queryMode = 'short_issue',
    riskTags = [],
    stage = 'planner',
    maxExamples = 6,
} = {}) => {
    const selected = [];
    const normalizedRiskTags = normalizeRiskTags(riskTags);
    const sameDomain = EXAMPLES.filter((example) => example.primaryDomain === primaryDomain);
    const sameDomainPositive = orderExamples(
        sameDomain.filter((example) => example.kind === 'positive'),
        { primaryDomain, queryMode, riskTags: normalizedRiskTags, stage }
    );
    const sameDomainRisk = orderExamples(
        sameDomain.filter((example) => example.kind !== 'positive' && example.riskTags?.some((tag) => normalizedRiskTags.includes(normalizeRiskTag(tag)))),
        { primaryDomain, queryMode, riskTags: normalizedRiskTags, stage }
    );
    const sameDomainAnti = orderExamples(
        sameDomain.filter((example) => example.kind !== 'positive'),
        { primaryDomain, queryMode, riskTags: normalizedRiskTags, stage }
    );
    const crossDomainRisk = orderExamples(
        EXAMPLES.filter((example) =>
            example.primaryDomain !== primaryDomain
            && example.kind !== 'positive'
            && example.riskTags?.some((tag) => normalizedRiskTags.includes(normalizeRiskTag(tag)))
        ),
        { primaryDomain, queryMode, riskTags: normalizedRiskTags, stage }
    );

    pushExamples(selected, sameDomainPositive.slice(0, 2), { limit: maxExamples });
    pushExamples(selected, sameDomainRisk.slice(0, stage === 'reviewer' ? 3 : 2), { limit: maxExamples });

    if (selected.length < maxExamples) {
        pushExamples(selected, sameDomainAnti, { limit: maxExamples });
    }

    if (selected.length < maxExamples) {
        pushExamples(selected, crossDomainRisk, { limit: maxExamples });
    }

    if (selected.length < maxExamples) {
        pushExamples(selected, sameDomainPositive, { limit: maxExamples });
    }

    return selected.slice(0, maxExamples);
};

const formatPlan = (plan = {}) => JSON.stringify(normalizePlanSnippet(plan), null, 2);

export const renderFewShotExamples = (examples = []) =>
    (Array.isArray(examples) ? examples : [])
        .map((example, index) => {
            const parts = [
                `ORNEK ${index + 1} [${example.id}]`,
                `Tur: ${example.kind}`,
                `Alan: ${example.primaryDomain}`,
                `Sorgu modu: ${example.queryMode}`,
                'Girdi:',
                normalizeDisplayText(example.inputText),
            ];

            if (example.badPlan) {
                parts.push('Yanlis plan:');
                parts.push(formatPlan(example.badPlan));
            }

            parts.push('Dogru plan:');
            parts.push(formatPlan(example.goodPlan));
            parts.push(`Neden: ${normalizeDisplayText(example.why)}`);
            return parts.join('\n');
        })
        .join('\n\n');

